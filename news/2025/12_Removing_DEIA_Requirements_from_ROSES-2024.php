<h1>Program News and Announcements</h1>

<p>4 February 2025</p>

<h2>Removing DEIA Requirements from ROSES-2024</h2>

<p>On 20 January 2025, the President of the United States of America signed an Executive Order entitled “Initial Rescissions of Harmful Executive Orders and Actions.” This Order repealed Executive Order 14035, “Diversity, Equity, Inclusion and Accessibility in the Federal Workforce.” It also directed agencies to take immediate action to identify and terminate DEIA initiatives and programs. To comply with this order, NASA’s <a href="https://science.nasa.gov/" target="_blank">Science Mission Directorate (SMD)</a> is in the process of amending open Program Elements in ROSES-2024 to end the Inclusion Plan Pilot Study, remove requirements for Inclusion Plans and the evaluation factors associated with them, and adjust the content of some other Program Elements to remove references to NASA’s DEIA programs. Program elements that have already received proposals will not be amended, but if they required Inclusion Plans, those plans will not be reviewed and will not impact the selection of proposals. The program elements relevant to PhysCOS scientists that will be amended include:</p>

<ul>
    <li><a href="https://nspires.nasaprs.com/external/solicitations/summary!init.do?solId=%7BEE77611A-DA28-E724-A4B6-F838EF9CD1DD%7D&path=open" target="_blank">D.3 Astrophysics Research and Analysis</a></li>
    <li><a href="https://nspires.nasaprs.com/external/solicitations/summary!init.do?solId=%7B818D9AFA-16F8-6F4D-9895-B879EE968932%7D&path=open" target="_blank">D.7 Strategic Astrophysics Technology</a></li>
    <li><a href="https://nspires.nasaprs.com/external/solicitations/summary!init.do?solId=%7B13570A2B-EE5C-553E-B73A-6312B84CFDAA%7D&path=open" target="_blank">D.14 Nancy Grace Roman Space Telescope Research and Support Participation Opportunities</a></li>
    <li><a href="https://nspires.nasaprs.com/external/solicitations/summary!init.do?solId=%7B05108FA8-5F27-3547-BBAF-0284D97BA62F%7D&path=open" target="_blank">D.15 LISA Preparatory Science</a></li>
</ul>

<p>In addition to these ROSES-24 programs, other SMD solicitations will be updated to comply with the Executive Order.</p>

<p>Please direct questions regarding any particular program element to the point of contact for that given in the summary table of key information at the end of the program element and at <a href="https://science.nasa.gov/researchers/sara/program-officers-list/" target="_blank">https://science.nasa.gov/researchers/sara/program-officers-list/</a>. Direct questions regarding the ROSES Summary of Solicitation to <a href="mailto:sara@nasa.gov">sara@nasa.gov</a>.</p>



