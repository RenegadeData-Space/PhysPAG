<h1>Program News and Announcements</h1>

<p>2 December 2024</p>

<h2>ROSES-24: F.5 FINESST: SMD’s Graduate Student Research Final Text and Due Date Released</h2>

<p><a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7b2AAB44BB-6DFB-C42E-315A-DC2D70683A9D%7d&path=&method=init" target="_blank">Future Investigators in NASA Earth and Space Science and Technology (FINESST F.5)</a> solicits proposals for graduate student-designed and performed research projects relevant to NASA’s Science Mission Directorate. The graduate student shall be the primary author, with input or supervision from the proposal's Principal Investigator (PI) or mentor, as appropriate. FINESST awards are research grants for up to three years at up to $50K per year.</p>

<p>ROSES-2024 Amendment 63 releases final text and due dates for <a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7b2AAB44BB-6DFB-C42E-315A-DC2D70683A9D%7d&path=&method=init" target="_blank">F.5 FINESST</a>, which had been listed as "TBD." An optional preproposal conference will occur on <strong>6 December 2024</strong>, at <strong>3p Eastern</strong> (12 noon Pacific); see Section 12.11 for connection information.</p>

<p>Notices of Intent are not requested, and proposals are due <strong>5 February 2025</strong>. Submissions must be formatted for Dual Anonymous Peer Review; see Section 4.2.</p>

<p>Please direct questions concerning F.5 FINESST to <a href="mailto:HQ-FINESST@mail.nasa.gov">HQ-FINESST@mail.nasa.gov</a>.</p>




