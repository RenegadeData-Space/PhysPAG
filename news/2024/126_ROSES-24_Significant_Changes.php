<h1>Program News and Announcements</h1>

<p>2 December 2024</p>

<h2>ROSES-24: Significant Changes to D.3 APRA, D.7 SAT, and D.8 RTF</h2>

<p><a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7bEE77611A-DA28-E724-A4B6-F838EF9CD1DD%7d&path=&method=init" target="_blank">D.3 Astrophysics Research and Analysis Program (APRA)</a> solicits basic research proposals for investigations that are relevant to NASA's programs in astronomy and astrophysics and includes research over the entire range of photons, gravitational waves, and particle astrophysics. Awards may be for up to four years’ duration (up to five years for Suborbital Investigations), but shorter-term proposals are typical.</p>

<p><a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7b818D9AFA-16F8-6F4D-9895-B879EE968932%7d&path=&method=init" target="_blank">D.7 Strategic Astrophysics Technology (SAT) is designed to support the maturation of key technologies for which feasibility has already been demonstrated (i.e., TRL 3), for potential infusion in spaceflight missions (TRL 6). Eligible missions are first, the Habitable Worlds Observatory, and secondarily, a future far-infrared observatory and a future X-ray observatory.</a></p>

<p><a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7b40F8A0E3-0053-AFB7-03A6-CEE078C08DB8%7d&path=&method=init" target="_blank">D.8 Nancy Grace Roman Technology Fellowships (RTF)</a> in astrophysics are to provide early-career researchers the opportunity to develop the skills necessary to lead astrophysics flight instrument development projects, including suborbital investigations, in preparation to become Principal Investigators (PIs) of future NASA astrophysics missions; to develop innovative technologies for space astrophysics that have the potential to enable major scientific breakthroughs; and to foster new talents by putting early career instrument builders on a trajectory towards long-term positions.</p>
 
<p>ROSES-2024 Amendment 64 announces several changes to the three Astrophysics Program Elements listed above.</p>
 
<p>For <a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7bEE77611A-DA28-E724-A4B6-F838EF9CD1DD%7d&path=&method=init" target="_blank">D.3 APRA</a>, the upper size for CubeSats has increased from 12 to 16U; Section 1.2.2.2 has been clarified to indicate that if the Proposer-Provided Suborbital Launch Vehicle option is chosen, the PI is responsible for costs of the integration and launch; Section 1.2.2.3 notes that CubeSat proposers should expect to participate in regular (bi-monthly to monthly) progress update meetings with NASA program management; POCs have been updated. The due dates are unchanged: mandatory notice of intent (NoI) is due <strong>13 December 2024</strong>; the final proposal is due <strong>30 January 2025</strong>.</p>
 
<p>For <a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7b818D9AFA-16F8-6F4D-9895-B879EE968932%7d&path=&method=init" target="_blank">D.7 SAT</a> the Notice of Intent to propose is no longer mandatory; the anticipated budget has been reduced to $5M for the first year of new selections; technology readiness level definitions have been clarified; updates have been made to exclusions, and the frequency of reporting for PhysCOS/COR has increased. The final proposal due date is unchanged: <strong>30 January 2025</strong>.</p>

<p>NASA is currently not planning to solicit SAT in ROSES-2025.</p>
 
<p>Finally, for <a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7b40F8A0E3-0053-AFB7-03A6-CEE078C08DB8%7d&path=&method=init" target="_blank">D.8 RTF</a> the page length for the fellowship application has been increased to two pages and the Evaluation Criteria for Fellowship have been expanded and clarified.</p>
 
<p>In addition to some significant changes listed here, the text of all three elements has been clarified and corrected in several additional small ways. New text is in bold and deleted text is struck through.</p>
 
<p>In all cases, Proposers are encouraged to use the NASA Grant Policy templates for <a href="https://view.officeapps.live.com/op/view.aspx?src=https%3A%2F%2Fwww.nasa.gov%2Fwp-content%2Fuploads%2F2024%2F09%2Fbiosketch-form.docx&wdOrigin=BROWSELINK" target="_blank">Biographical Sketch [DOCX]</a> (no page limit) and <a href="https://view.officeapps.live.com/op/view.aspx?src=https%3A%2F%2Fwww.nasa.gov%2Fwp-content%2Fuploads%2F2024%2F09%2Fcurrent-and-pending-support-cps-form.docx&wdOrigin=BROWSELINK" target="_blank">current and pending support [DOCX]</a> that were released 10/1/24. For more information see <a href="https://science.nasa.gov/researchers/solicitations/roses-2024/amendment-57-roses-24-changes-for-2-cfr-200-update/" target="_blank">Amendment 57</a> to the <a href="https://nspires.nasaprs.com/external/viewrepositorydocument/cmdocumentid=983947/solicitationId=%7b3314EF2F-63D5-9A7C-0E74-1F9A8ABB4332%7d/viewSolicitationDocument=1/ROSES24_SoS_111524.pdf" target="_blank">ROSES-24 Summary of Solicitation [PDF]</a>.</p>
 
<p>Please direct questions concerning <a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7bEE77611A-DA28-E724-A4B6-F838EF9CD1DD%7d&path=&method=init" target="_blank">D.3 APRA</a> to David C. Morris at <a href="mailto:david.c.morris@nasa.gov">david.c.morris@nasa.gov</a>, and questions concerning <a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7b818D9AFA-16F8-6F4D-9895-B879EE968932%7d&path=&method=init" target="_blank">D.7 SAT</a> and <a href="https://nspires.nasaprs.com/external/solicitations/summary.do?solId=%7b40F8A0E3-0053-AFB7-03A6-CEE078C08DB8%7d&path=&method=init" target="_blank">D.8 RTF</a> to Dominic Benford at <a href="mailto:dominic.benford@nasa.gov">dominic.benford@nasa.gov</a>.</p>



