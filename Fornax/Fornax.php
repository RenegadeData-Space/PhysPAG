    <li>Fornax Presentation at AAS 245, Session 347, January 2025 [<a href="/Fornax/presentations/347.04 Sparke Open Science in the Cloud.pdf" target="_blank">PDF</a>]</li>
    <li>The Fornax Initiative: Cloud Computing with Astrophysics Data Jan. 2025 [<a href="/Fornax/presentations/Jaffe_Fornax_PhysCOS.pdf" target="_blank">PDF</a>]</li>
</ul>

<hr>


<p style="margin-top: 2rem;">NASA's Astrophysics missions generate vast and complex datasets offering immense scientific potential. The vital role of NASA's Astrophysics archives in mission success is evident, as they have demonstrated that user-focused, technologically-rich data systems are key science multipliers. As the Astronomy 2020 Decadal Review emphasized, in the coming decade, frontier science will be done with multi-wavelength and multi-messenger analysis across large, complex data sets, which will only increase the challenges of accessing big data, maintaining software, and obtaining sufficient computing resources.</p>

<p><strong>NASA Astrophysics is developing the Fornax Initiative, a cloud-based system that brings together data, software, and computing so that researchers can focus on science.</strong></p>

<h3 class="spaced-bottom">The three pillars of the Fornax Initiative project are:</h3>

<div class="indent-box">

<p class="indent-box"><strong>Fornax Scientific Components</strong>, the astrophysics-specific elements required to enable science in the cloud, including Python notebooks that demonstrate access to cloud-hosted NASA mission data, curated astrophysics software environments, and cloud-native services to support common astronomy workflows.</p>

<p><strong>Fornax Science Console</strong>, a web-based application that users log into for access to cloud computing, data storage, and interactive data analysis in JupyterLab.</p>

<p><strong>Science Support Systems</strong>, a program of engagement with the astronomical community, including a Helpdesk and training opportunities.</p>
</div>

<div class="sigImage" style="float: left; padding: 30px 0px 10px 30px;"><a href="/fornax/images/Scientific_info.png"><img alt="Scientific Information Graphic" src="/fornax/images/Scientific_info.png"></a></div>

<div class="clear"></div>

<p>Our goal is to support the science needs of a wide range of users, from those who are learning Python and would simply benefit from having access to a maintained Python environment for common astrophysics software to those who wish to perform complicated analyses that require significant cloud computing. For use cases that require more computing than can be provided by NASA through Fornax, we will build the system such that scientists can use the Fornax Scientific Components and the Fornax Science Console with their own cloud computing resources.</p>

