<h1>TDAMM Workshops </h1>

<p>TDAMM Workshops are intended to provide a forum for sharing the latest scientific and technical information, incubating new ideas and improvements to the status quo, developing skills, and connecting the diverse TDAMM community. Participants produce a white paper summarizing the outcomes following each workshop.</p>

<h2>4th TDAMM Workshop</h2>

<ul>
    <li>4th TDAMM Workshop High-Resolution Poster with QR Code [<a href="/multimessenger/documents/HSV.pdf" target="_blank">PDF</a>]</li>
</ul>

<p>Theme: Community-defined coordinated observing concepts for rare and important transient events.</p> 

<p>Motivation: Define and prioritize science cases, triggering criteria, and the essential follow-up observations&#8212;ground and space, public and private&#8212;desired by the community so that observatory science teams can pre-coordinate plans and efficiently execute community-driven observations.</p>

<p>Date: 27 &#8211; 30 October 2025</p>

<p>Host: University of Alabama, Huntsville / USRA</p>

