<h1>PhysCOS TDAMM Initiative</h1>

<div class="sigImage" style="float: right; padding: 20px 5px 10px 20px;"><figure><img src="/multimessenger/images/Neutron-Stars-Collide3.gif"><figcaption>Animation: Doomed Neutron Stars Create Blast of Light and Gravitational Waves. Credit: <a href="https://svs.gsfc.nasa.gov/12740" target="_blank">NASA Goddard Space Flight Center / CI Lab</a></figcaption></figure></div>

<p>In response to the Astro2020 decadal report’s recommendation that <strong>time-domain and multi-messenger (TDAMM)</strong> astrophysics is the highest-priority sustaining activity in space, the NASA Astrophysics Division directed the Physics of the Cosmos Program Office to:</p>
<ol style="1">
    <li>Organize or support <strong>TDAMM Workshops</strong> at regular intervals,</li>
    <li>Coordinate with the relevant community-driven <strong>Science Interest Groups and Science Analysis Groups</strong>,</li> 
    <li>Conduct a three-phase <strong>TDAMM Study</strong> investigating ways to improve coordination within the NASA fleet (Phase 1), with U.S. ground-based observatories (Phase 2), and internationally (Phase 3), and</li>
    <li>Recommend <strong>implementation strategies</strong> for enabling TDAMM science.</li> 
</ol>

<p>The Astrophysics Cross-Observatory Science Support (ACROSS) pilot is an outcome of the TDAMM Study, which identified needs for:</p>
<ol>
    <li>A <strong>web portal</strong> providing access to open-source software and data systems that facilitate cross-observatory workflows,</li>
    <li>A <strong>help desk</strong> to provide subject-matter expertise and facilitate coordination, and</li>
    <li>An <a href="https://science.nasa.gov/open-science/" target="_blank"><strong>open science</strong></a> <strong>community grant program</strong> to incentivize innovation.</li>  
</ol>

<p>The PhysCOS TDAMM Initiative executes the above tasks on behalf of the Astrophysics Division and leads implementation of the ACROSS pilot.</p> 


